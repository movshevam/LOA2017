class ifelseif{
  public static void main(String[] args){
    int x = 5;
    int y = 8;

    if(x > y){
      System.out.println("1st if works");
    }

    else if(x > 2*y){
      System.out.println("1st else if works");
    }

    else{
      System.out.println("else works");
    }
    





  }
}
